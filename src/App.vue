<template>
  <div id="app">
    <img src="./assets/logo.png">
    <TodoList v-bind:todos="todos"/>
    <create-todo v-on:create-todo="addTodo"></create-todo>
  </div>
</template>

<script>
import TodoList from './components/TodoList'
import CreateTodo from './components/CreateTodo'

export default {
  name: 'App',
  components: {
    TodoList,
    CreateTodo
  },
  data () {
    return {
      todos: [{
        title: 'Todo A',
        project: 'Project A',
        done: true
      }, {
        title: 'Todo B',
        project: 'Project B',
        done: false
      }, {
        title: 'Todo C',
        project: 'Project C',
        done: false
      }]
    }
  },
  methods: {
    addTodo (todo) {
      this.todos.push({
        title: todo.title,
        project: todo.project,
        done: false
      })
    }
  }
}
</script>

